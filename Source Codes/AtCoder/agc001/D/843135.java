  private static class Task {
    void solve(FastScanner in, PrintWriter out) {
      int N = in.nextInt();
      int M = in.nextInt();
      int[] A = new int[M];
      for (int i = 0; i < M; i++) A[i] = in.nextInt();
      if (M == 1) {
        out.println(A[0]);
        if (A[0] <= 2) {
          out.println(1);
          out.println(A[0]);
          return;
        }
        out.println(2);
        out.println((A[0] - 1) + " 1");
        return;
      }

      ArrayDeque<Integer> odds = new ArrayDeque<>();
      ArrayDeque<Integer> evens = new ArrayDeque<>();
      for (int a : A) {
        if (a % 2 == 0) evens.add(a);
        else odds.add(a);
      }
      if (odds.size() > 2) {
        out.println("Impossible");
        return;
      }

      ArrayList<Integer> a = new ArrayList<>();
      if (!odds.isEmpty()) a.add(odds.poll());
      while (!evens.isEmpty()) a.add(evens.poll());
      if (!odds.isEmpty()) a.add(odds.poll());
      for (int i : a) out.print(i + " ");
      out.println();
      out.println(a.get(0) == 1 ? a.size() - 1 : a.size());
      for (int i = 0; i < a.size() - 1; i++) {
        if (i == 0 && a.get(i) == 1) continue;
        if (i == 0) out.print(a.get(i) - 1);
        else out.println(a.get(i));
        out.print(" ");
      }
      out.println(a.get(a.size() - 1) + 1);
    }
  }

