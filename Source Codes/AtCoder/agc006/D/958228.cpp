#include <iostream>
#define int long long
using namespace std;

int n;
int a[200000];

bool check(int mid) {
	static int t[200000];
	int i;
	
	for (i = 0; i < 2 * n - 1; i++) { t[i] = (a[i] >= mid); }
	
	int score[2] = {1145141919, 1145141919};
	for (i = 0; i < 2 * n - 2; i++) {
		if (t[i] == 0 && t[i+1] == 0) {
			score[0] = min(score[0], min(abs(n - 1 - i), abs(n - 1 - (i+1))));
		}
		if (t[i] == 1 && t[i+1] == 1) {
			score[1] = min(score[1], min(abs(n - 1 - i), abs(n - 1 - (i+1))));
		}
	}
	
	if (score[0] == 1145141919 && score[1] == 1145141919) {
		return (t[0] == 1);
	}
	return score[1] < score[0];
}

void input() {
	cin >> n;
	for (int i = 0; i < 2 * n - 1; i++) cin >> a[i];
}

signed main() {
	input();
	
	int st = 1, ed = 2 * n - 1, mid;	//oooooxxx…, [st, ed)
	while (ed - st >= 2) {
		mid = (st + ed) / 2;
		if (check(mid)) {
			st = mid;	//[mid, ed)
		}
		else {
			ed = mid;	//[st, mid)
		}
	}
	cout << st << endl;
	return 0;
}