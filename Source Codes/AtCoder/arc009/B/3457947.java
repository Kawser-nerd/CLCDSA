	static class TaskX {

		public void solve(int testNumber, InputReader in, PrintWriter out) {

			Map<Character, Character> map = new HashMap<>();
			for (char i = '0'; i <= '9'; i++) {
				map.put(in.nextString().charAt(0), i);
			}

			int n = in.nextInt();
			P[] p = new P[n];
			char[][] s = new char[n][];
			for (int i = 0; i < n; i++) {
				s[i] = in.nextString().toCharArray();
				char[] t = new char[s[i].length];
				for (int j = 0; j < s[i].length; j++) {
					if (!map.containsKey(s[i][j])) continue;
					t[j] = map.get(s[i][j]);
				}
				p[i] = new P(new String(s[i]), Integer.parseInt(new String(t)));
			}
			Arrays.sort(p);

			for (P pp : p) {
				out.println(pp.s);
			}
		}
	}

	static class P implements Comparable<P> {
		String s;
		int m;
		public P(String s, int m) {
			super();
			this.s = s;
			this.m = m;
		}
		@Override
		public int compareTo(P o) {
			return Integer.compare(this.m, o.m);
		}
		@Override
		public String toString() {
			return "P [s=" + s + ", m=" + m + "]";
		}
	}

	static class InputReader {
		BufferedReader in;
		StringTokenizer tok;

		public String nextString() {
			while (!tok.hasMoreTokens()) {
				try {
					tok = new StringTokenizer(in.readLine(), " ");
				} catch (IOException e) {
					throw new InputMismatchException();
				}
			}
			return tok.nextToken();
		}

		public int nextInt() {
			return Integer.parseInt(nextString());
		}

		public long nextLong() {
			return Long.parseLong(nextString());
		}

		public double nextDouble() {
			return Double.parseDouble(nextString());
		}

		public int[] nextIntArray(int n) {
			int[] res = new int[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextInt();
			}
			return res;
		}

		public int[] nextIntArrayDec(int n) {
			int[] res = new int[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextInt() - 1;
			}
			return res;
		}

		public int[] nextIntArray1Index(int n) {
			int[] res = new int[n + 1];
			for (int i = 0; i < n; i++) {
				res[i + 1] = nextInt();
			}
			return res;
		}

		public long[] nextLongArray(int n) {
			long[] res = new long[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextLong();
			}
			return res;
		}

		public long[] nextLongArrayDec(int n) {
			long[] res = new long[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextLong() - 1;
			}
			return res;
		}

		public long[] nextLongArray1Index(int n) {
			long[] res = new long[n + 1];
			for (int i = 0; i < n; i++) {
				res[i + 1] = nextLong();
			}
			return res;
		}

		public double[] nextDoubleArray(int n) {
			double[] res = new double[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextDouble();
			}
			return res;
		}

		public InputReader(InputStream inputStream) {
			in = new BufferedReader(new InputStreamReader(inputStream));
			tok = new StringTokenizer("");
		}
	}

