using System;
using System.Text;
using System.Collections.Generic;
using System.Linq;
class Solve{
    public Solve(){}
    StringBuilder sb;
    ReadData re;
    public static int Main(){
        new Solve().Run();
        return 0;
    }
    void Run(){
        sb = new StringBuilder();
        re = new ReadData();
        Calc();
        Console.Write(sb.ToString());
    }
    void Calc(){
        string S = re.s();
        int N = S.Length;
        long Rev = Pow(3,Define.mod-2);
        int nq = 0;
        for(int i=0;i<N;i++){
            if(S[i] == '?'){
                nq++;
            }
        }
        long[] A = new long[N];
        long[] C = new long[N];
        for(int i=0;i<N;i++){
            if(S[i] == 'A'){
                A[i]++;
            }
            if(S[i] == '?'){
                A[i]+=Rev;
            }
        }
        for(int i=1;i<N;i++){
            A[i] += A[i-1];
        }
        for(int i=0;i<N;i++){
            if(S[i] == 'C'){
                C[i]++;
            }
            if(S[i] == '?'){
                C[i]+=Rev;
            }
        }
        for(int i=N-2;i>=0;i--){
            C[i] += C[i+1];
        }
        for(int i=0;i<N;i++){
            A[i] %= Define.mod;
            C[i] %= Define.mod;
        }
        long count1 = 0;
        long count2 = 0;
        for(int i=1;i<N-1;i++){
            if(S[i] == '?'){
                count1 += A[i-1]*C[i+1]%Define.mod;
                count1 %= Define.mod;
            }
            if(S[i] == 'B'){
                count2 += A[i-1]*C[i+1]%Define.mod;
                count2 %= Define.mod;
            }
        }
        long count = count1 * Pow(3,nq-1) % Define.mod + count2 * Pow(3,nq) % Define.mod;
        count %= Define.mod;
        sb.Append(count+"\n");
    }
    long Pow(long X,long R){
        if(R < 0){
            return 0;
        }
        if(R == 0){
            return 1;
        }
        if(R % 2 == 0){
            long t = Pow(X,R/2);
            return t*t%Define.mod;
        }
        return Pow(X,R-1)*X%Define.mod;
    }
}
