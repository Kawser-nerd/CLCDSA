def b_stripe(N, C, A):
    from itertools import permutations
    # ???????????????
    used_num_list = list(set(A))

    # 1?????????????????????????????
    if len(used_num_list) == 1:
        return C * (len(A) // 2)

    # ?????????????2?????????first, second????
    # ???????(0-based)????first?????????second??????
    # ?????????????????????????
    ans = float('inf')
    for first, second in permutations(used_num_list, 2):
        tmp_cost = 0
        for k, a in enumerate(A):
            if k % 2 == 0 and a != first:
                tmp_cost += C
            elif k % 2 == 1 and a != second:
                tmp_cost += C
        ans = min(ans, tmp_cost)
    return ans

N, C = [int(i) for i in input().split()]
A = [int(input()) for _ in range(N)]
print(b_stripe(N, C, A))