import java.util.ArrayList;
import java.util.Collections;
import java.util.Scanner;

public class Main {
	public static void main(String[] args) {

		// ?????????????????????????????
		Scanner sc = new Scanner(System.in);
		int videoCnt = sc.nextInt();
		int timesBrowsableVideos = sc.nextInt();

		ArrayList<Integer> deliveryPersonRateList = new ArrayList<Integer>();
		for (int idx = 0; idx < videoCnt; idx++) {
			deliveryPersonRateList.add(sc.nextInt());
		}
		// ????????
		Collections.sort(deliveryPersonRateList);
		Collections.reverse(deliveryPersonRateList);

		// ???????????????????????????????
		ArrayList<Integer> list = new ArrayList<Integer>();
		for (int idx = 0; idx < timesBrowsableVideos; idx++) {
			list.add(deliveryPersonRateList.get(idx));
		}
		Collections.sort(list);

		// ???????
		double totalRate = 0;

		// ????????????????
		if (timesBrowsableVideos == 1) {
			// ???????1?????????????????????
			for (double rate : list) {
				totalRate = rate > totalRate ? rate : totalRate;
			}
			totalRate = totalRate / 2;
		} else {
			// ??????????????????????????????????????????????
			for (double rate : list) {
				totalRate = (totalRate + rate) / 2;
			}
		}

		System.out.println(totalRate);
		sc.close();
	}
}