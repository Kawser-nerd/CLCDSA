using System.Linq;
class Solve{
    Node Top;
    public Solve(){}
    StringBuilder sb;
    ReadData re;
    public static int Main(){
        new Solve().Run();
        return 0;
    }
    void Run(){
        sb = new StringBuilder();
        re = new ReadData();
        Calc();
        Console.Write(sb.ToString());
    }
    void Calc(){
        Top = new Node();
        int N = re.i();
        long L = re.l();
        for(int i=0;i<N;i++){
            Add(re.s(),0,Top);
        }
        if(Calc(Top,L) == 0){
            sb.Append("Bob\n");
        }
        else{
            sb.Append("Alice\n");
        }
    }
    long Calc(Node V,long L){
        if(V.LeftNode == null && V.RightNode == null){
            return 0;
        }
        long ans = 0;
        if(V.LeftNode == null){
            ans ^= ToNim(L);
        }
        else{
            ans ^= Calc(V.LeftNode,L-1);
        }
        if(V.RightNode == null){
            ans ^= ToNim(L);
        }
        else{
            ans ^=Calc(V.RightNode,L-1);
        }
        return ans;
    }
    void Add(string S,int p,Node V){
        if(p != S.Length){
            if(S[p] == '0'){
                if(V.LeftNode == null){
                    V.LeftNode = new Node();
                }
                Add(S,p+1,V.LeftNode);
            }
            else{
                if(V.RightNode == null){
                    V.RightNode = new Node();
                }
                Add(S,p+1,V.RightNode);
            }
        }
    }
    long ToNim(long val){
        long ans = 1;
        while(val % 2 == 0){
            ans *= 2;
            val /= 2;
        }
        return ans;
    }
}
class Node{
    public Node LeftNode;
    public Node RightNode;
    public Node(){}
}
class ReadData{
    string[] str;
    int counter;
    public ReadData(){
        counter = 0;
    }
    public string s(){
        if(counter == 0){
            str = Console.ReadLine().Split(' ');
            counter = str.Length;
        }
        counter--;
        return str[str.Length-counter-1];
    }
    public int i(){
        return int.Parse(s());
    }
    public long l(){
        return long.Parse(s());
    }
    public double d(){
        return double.Parse(s());
    }
    public int[] ia(int N){
