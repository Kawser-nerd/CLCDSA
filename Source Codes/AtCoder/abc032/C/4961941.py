N, K = list(map(int, input().split()))
s = [int(input()) for i in range(N)]

def main():
    if 0 in s:
        return N
    else:
        l, r = 0, 0
        val = 1
        ans = 0
        # ????
        while r < N:
            # ????????????????????K????????????
            if val*s[r] <= K:
                val *= s[r]
                r += 1
                ans = max(ans, r-l)
            # ?????????????????????????
            # ?????????????????
            elif l == r:
                l += 1
                r += 1
            # ?????????????
            else:
                val //= s[l]
                l += 1
            # print('l={}, r={}, size={}, {}'.format(l, r, r - l, s[l:r]))

        return ans

print(main())