class Program
{
    static void Main(string[] args)
    {
        var num = Input.num;
        var NUM = 55556;
        var bo = new bool[NUM];
        var list = new List<int>();
        for(var i=2;i<NUM;i++)
            if (!bo[i])
            {
                if (i % 5 == 1 && list.Count != num) list.Add(i);
                for (var j = 2 * i; j < NUM; j += i)
                    bo[j] = true;
            }
        WriteLine(string.Join(" ", list));
    }
}

