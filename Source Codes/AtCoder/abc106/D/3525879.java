	static class TaskX {

		public void solve(int testNumber, InputReader in, PrintWriter out) {

			int N = in.nextInt(), M = in.nextInt(), Q = in.nextInt();
			int[][] mat = new int[N+1][N+1];
			int[][] sum = new int[N+1][N+1];

			for (int i = 0; i < M; i++) {
				int l = in.nextInt(), r = in.nextInt();
				mat[l][r]++;
			}

			for (int l = 0; l < N+1; l++) {
				for (int r = 0; r < N+1; r++) {
					sum[l][r] = mat[l][r];
					if (l > 0) sum[l][r] += sum[l-1][r];
					if (r > 0) sum[l][r] += sum[l][r-1];
					if (l > 0 && r > 0) sum[l][r] -= sum[l-1][r-1];
				}
			}

			while (Q-- > 0) {
				int p = in.nextInt(), q = in.nextInt();
				out.println(calc(p, p, q, q, sum));
			}
		}

		int calc(int x1, int y1, int x2, int y2, int[][] sum) {
			int ret = sum[x2][y2];
			if (x1 > 0) ret -= sum[x1-1][y2];
			if (y1 > 0) ret -= sum[x2][y1-1];
			if (x1 > 0 && y1 > 0) ret += sum[x1-1][y1-1];
			return ret;
		}
	}

	static class InputReader {
		BufferedReader in;
		StringTokenizer tok;

		public String nextString() {
			while (!tok.hasMoreTokens()) {
				try {
					tok = new StringTokenizer(in.readLine(), " ");
				} catch (IOException e) {
					throw new InputMismatchException();
				}
			}
			return tok.nextToken();
		}

		public int nextInt() {
			return Integer.parseInt(nextString());
		}

		public long nextLong() {
			return Long.parseLong(nextString());
		}

		public double nextDouble() {
			return Double.parseDouble(nextString());
		}

		public int[] nextIntArray(int n) {
			int[] res = new int[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextInt();
			}
			return res;
		}

		public int[] nextIntArrayDec(int n) {
			int[] res = new int[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextInt() - 1;
			}
			return res;
		}

		public int[] nextIntArray1Index(int n) {
			int[] res = new int[n + 1];
			for (int i = 0; i < n; i++) {
				res[i + 1] = nextInt();
			}
			return res;
		}

		public long[] nextLongArray(int n) {
			long[] res = new long[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextLong();
			}
			return res;
		}

		public long[] nextLongArrayDec(int n) {
			long[] res = new long[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextLong() - 1;
			}
			return res;
		}

		public long[] nextLongArray1Index(int n) {
			long[] res = new long[n + 1];
			for (int i = 0; i < n; i++) {
				res[i + 1] = nextLong();
			}
			return res;
		}

		public double[] nextDoubleArray(int n) {
			double[] res = new double[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextDouble();
			}
			return res;
		}

		public InputReader(InputStream inputStream) {
			in = new BufferedReader(new InputStreamReader(inputStream));
			tok = new StringTokenizer("");
		}
	}

