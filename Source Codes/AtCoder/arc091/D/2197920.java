	static int nim(long n, int K)
	{
		while(true){
			tr(n);
			if(n < K)return 0;
			if(n % K == 0)return (int)(n/K);
			long s = n/K+1;
			// n-1-t*s >= uK
			long skip = Math.max(1, n%K/s);
			n -= skip*s;
		}
	}
