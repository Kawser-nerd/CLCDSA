	void submit() {
		int n = nextInt();
		int s = nextInt();
		int[] xs = new int[n];
		long[] ps = new long[n];
		for (int i = 0; i < n; i++) {
			xs[i] = nextInt();
			ps[i] = nextInt();
		}
		
		ArrayList<Integer> order = new ArrayList<>();
		
		int l = 0, r = n - 1;
		while (true) {
			if (xs[l] >= s) {
				for (int i = r; i >= l; i--) {
					order.add(i);
				}
				break;
			}
			if (xs[r] <= s) {
				for (int i = l; i <= r; i++) {
					order.add(i);
				}
				break;
			}
			if (ps[l] >= ps[r]) {
				ps[l] += ps[r];
				order.add(r);
				r--;
			} else {
				ps[r] += ps[l];
				order.add(l);
				l++;
			}
		}
		
		Collections.reverse(order);
		
		long ans = Math.abs(s - xs[order.get(0)]);
		for (int i = 1; i < n; i++) {
			ans += Math.abs(xs[order.get(i)] - xs[order.get(i - 1)]);
		}
		
		out.println(ans);
	}

