int main()
{
    cin.tie(0);
    ios_base::sync_with_stdio(false);
    lint n;
    cin >> n;
    vector<lint> a(n);
    lint ans = 1;
    for (int i=0; i<n; ++i){
        scanf("%lld", &a[i]);
    }
    int flag = 0; // 1 : <=, 2 : >=, 0 : undefined
    int l = 0;
    int r = 1;
    while(r < n){
        while(r < n){
            if(a[l] == a[r]){
                r++;
                continue;
            }
            else if(a[l] < a[r]){
                flag = 1;
                break;
            }
            else if(a[l] > a[r]){
                flag = 2;
                break;
            }
        }
        while(r < n){
            if(flag == 1){
                if(a[r-1] > a[r]){
                    ans++;
                    break;
                }
                else {
                    r++;
                }
            }
            else if(flag == 2){
                if(a[r-1] < a[r]){
                    ans++;
                    break;
                }
                else {
                    r++;
                }
            }
        }
        // debugpair(l, r);
        l = r;
        r++;
    }
    cout << ans << "\n";
    return 0;
}