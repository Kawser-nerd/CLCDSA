N = int(input())

upper_nums = [] #???????????????????????list

for _ in range(N):
    new_num = int(input()) #???????????
    if len(upper_nums) == 0:
        upper_nums.append(new_num)
    else:
        for i in range(len(upper_nums)):
            if upper_nums[i] >= new_num:
                upper_nums[i] = new_num
                break
        else:
            upper_nums.append(new_num)
    upper_nums.sort()

print(len(upper_nums))