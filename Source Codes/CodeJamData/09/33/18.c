int bribe(i)
{
  int res,j;
  res=0;
  if(cells[i]==0)
    return 0;
  //left
  j=i-1;
  while(cells[j]==1 && j>=0)
  {
    res++;
    j--;
  }
  //right
  j=i+1;
  while(cells[j]==1 && j<P)
  {
    res++;
    j++;
  }
  return res;
}
void cleancells()
{
  int i;
  for(i=0;i<P;i++)
    cells[i]=1;
}

int main(int argc, char *argv[])
{
 char tc;
 int c,i,j,temp;

  //checking input parameters
  if(argc!=2)
    return 1;
  //opening input and output files
  fi = fopen(argv[1],"rt");
  fo = fopen("q1c_c.out","wt");
  //reading the  number of testcases
  if(!fscanf(fi,"%d",&T))
    return 1;
  for(c=0;c<T;c++){
    fscanf(fi,"%d %d",&P,&Q);
    for(i=0;i<Q;i++)
      fscanf(fi,"%d",&prisoners[i]);
    //input have been read
    for(i=Q;i<5;i++)
      prisoners[i]=prisoners[0];
    //calculating the result
    result=P*Q;
    for(i=0;i<120;i++)
    {
      //calculating one permutation
      temp=0;
      cleancells();
      for(j=0;j<5;j++)
      {
        temp+=bribe(prisoners[permutations[i][j]]-1);
        cells[prisoners[permutations[i][j]]-1]=0;
      }
      if(temp<result)
        result=temp;
    }
    fprintf(fo,"Case #%d: %d\n",c+1,result);
  }
  fclose(fi);
  fclose(fo);
  return 0;
}
