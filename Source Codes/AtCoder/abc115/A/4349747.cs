using System;

class Program
{
    static void Main()
    {
        switch (Console.ReadLine())
        {
            case "25":
                Console.WriteLine("Christmas");
                break;
            case "24":
                Console.WriteLine("Christmas Eve");
                break;
            case "23":
                Console.WriteLine("Christmas Eve Eve");
                break;
            case "22":
                Console.WriteLine("Christmas Eve Eve Eve");
                break;
        }
    }
}