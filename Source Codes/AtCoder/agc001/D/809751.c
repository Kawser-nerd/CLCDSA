#include <stdio.h>

int B[10000000];
int len;

void print(int x) {
	if(x > 0) B[len++]=x;
}

void yaru(int a, int islast) {
	int i;
	if (a%2==1) {
		for(i=1;i<a;i+=2) {
			print(2);
		}
		if(!islast) print(2); /* ???????????????? */
	} else {
		if((a/2)%2==0) {
			if(islast) {
				print(1);
				print(a-2);
			} else {
				print(a);
			}
		} else {
			print(a/2-1);
			if(islast) {
				print(1);
				print(a/2-1);
			} else {
				print(a/2+1);
			}
		}
	}
}

int main(void) {
	int N,M;
	int A[200];
	int i;
	int kisuucount=0;
	int k1=0,k2=0;
	int tmp;
	if(scanf("%d%d",&N,&M)!=2)return 1;
	for(i=0;i<M;i++) {
		if(scanf("%d",&A[i])!=1)return 1;
	}
	for(i=0;i<M;i++) {
		if(A[i]%2!=0) {
			if(kisuucount==0)k1=i; else k2=i;
			kisuucount++;
		}
	}
	if(kisuucount>2) {
		puts("Impossible");
		return 0;
	}
	if(kisuucount==1) {
		tmp=A[k1];
		A[k1]=A[0];
		A[0]=tmp;
	} else if(kisuucount==2) {
		tmp=A[k1];
		A[k1]=A[0];
		A[0]=tmp;
		tmp=A[k2];
		A[k2]=A[M-1];
		A[M-1]=tmp;
	}
	printf("%d", A[0]);
	for(i=1;i<M;i++) printf(" %d",A[i]);
	putchar('\n');
	len=0;
	if(A[0]%2==1) {
		yaru(A[0], M==1);
		if(M==1) print(1);
	} else {
		print(1);
		yaru(A[0], M==1);
	}
	for(i=1;i<M;i++) {
		yaru(A[i],i+1>=M);
	}
	printf("%d\n", len);
	printf("%d", B[0]);
	for(i=1;i<len;i++) printf(" %d", B[i]);
	putchar('\n');
	return 0;
}

