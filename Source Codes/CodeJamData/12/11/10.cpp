const int INF = 1ll<<25;

int main(){
  ios_base::sync_with_stdio(false) ;
  cout<<fixed;
  cout.precision(10);

  int ttime;
  cin>>ttime;
  for (int ccount=1;ccount<=ttime;ccount++){
    int n,m;
    cin>>n>>m;
    vector<double> prob(1,1);
    for (int i=1;i<=n;i++){
      double temp;
      cin>>temp;
      prob.push_back(temp*prob.back());
    }
    double ans = INF;
    for (int i=0;i<=n;i++){
      ans = min(ans,(i+(m-n+i)+1)*1+
		(m+1)*(1-prob[prob.size()-i-1]));
    }
    ans = min(ans,(double)m+2);
//     cout<<prob<<endl;
    cout<<"Case #"<<ccount<<": "<<ans<<endl;
  }
}
