int LongestIncreasingSubsequence(const std::vector<int> &a) {
    std::vector<int> dp(a.size(), 1 << 28);
    for (int i = 0; i < a.size(); ++i) {
        *std::lower_bound(dp.begin(), dp.end(), a[i]) = a[i];
    }
    return (std::lower_bound(dp.begin(), dp.end(), 1 << 28) - dp.begin());
}

int main() {
    int64_t x1, y1, x2, y2;
    in(x1, y1, x2, y2);

    int64_t n;
    in(n);

    vector<int64_t> x(n), y(n);
    for (int i : rep(0, n)) {
        in(x[i], y[i]);
    }

    if (x1 > x2) {
        for (int i : rep(0, n)) {
            x[i] *= -1;
        }

        x1 *= -1;
        x2 *= -1;
    }

    if (y1 > y2) {
        for (int i : rep(0, n)) {
            y[i] *= -1;
        }

        y1 *= -1;
        y2 *= -1;
    }

    vector<pair<int64_t, int64_t>> a;

    for (int i : rep(0, n)) {
        if (x1 <= x[i] && x[i] <= x2 && y1 <= y[i] && y[i] <= y2) {
            a.emplace_back(x[i], y[i]);
        }
    }

    sort(a);

    vector<int> b;

    for (auto p : a) {
        b.push_back(p.second);
    }

    int m = LongestIncreasingSubsequence(b);

    const long double pi = 3.141592653589793238462643383279;

    long double ans = (x2 - x1) * 100 + (y2 - y1) * 100;
    if (m - 1 == x2 - x1 || m - 1 == y2 - y1) {
        ans -= (m - 1) * (20 - 5 * pi) + (20 - 10 * pi);
    } else {
        ans -= m * (20 - 5 * pi);
    }

    out(ans);
}