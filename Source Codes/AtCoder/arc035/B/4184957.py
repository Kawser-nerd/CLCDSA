def b_contest(N, T):
    from itertools import accumulate
    from collections import Counter
    MOD = 10**9 + 7

    T.sort()
    penalty_time = sum(accumulate(T))  # ?????????????????????

    # ????????????????
    seq_count = Counter(T)
    seq_max_appear = max(seq_count.values())

    # ???????????????????????????????????
    # ???????????????????????????????????
    factorial = [1]
    for k in range(1, seq_max_appear + 1):
        factorial.append((factorial[-1] * k) % MOD)
    combinations = 1
    for v in seq_count.values():
        combinations *= factorial[v]
        combinations %= MOD

    ans = '{0}\n{1}'.format(penalty_time, combinations)
    return ans

N = int(input())
T = [int(input()) for _ in [0] * N]
print(b_contest(N, T))