#include<cstdio>

struct BIGINT
{
	int a[500002],sum,l;
	void read()
	{
		char c;
		while((c=getchar())>='0'&&c<='9')
			sum+=a[l++]=c-'0';
		for(int i=0;i<l>>1;i++)
			a[i]^=a[l-i-1]^=a[i]^=a[l-i-1];
	}

	void mul(int k)
	{
		for(int i=0,c=0;i<l||c;i++)
		{
			sum-=a[i];
			a[i]=a[i]*k+c;
			c=a[i]/10;
			sum+=a[i]%=10;
		}
	}

	void add(int c)
	{
		for(int i=0;c;i++)
		{
			sum-=a[i];
			a[i]+=c;
			c=a[i]/10;
			sum+=a[i]%=10;
		}
	}
}N;

int main()
{
	N.read(),N.mul(9);
	int k=0;
	do N.add(9),k++;
	while(N.sum>k*9);
	printf("%d\n",k);
}