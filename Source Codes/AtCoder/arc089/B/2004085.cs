using System;
using System.Text;
using System.Collections.Generic;
using System.Linq;
class Solve{
    int[,] Map;
    int K;
    public Solve(){}
    StringBuilder sb;
    ReadData re;
    public static int Main(){
        new Solve().Run();
        return 0;
    }
    void Run(){
        sb = new StringBuilder();
        re = new ReadData();
        Calc();
        Console.Write(sb.ToString());
    }
    void Calc(){
        int N = re.i();
        K = re.i();
        Map = new int[K,K*2];
        int count = 0;
        for(int i=0;i<N;i++){
            int x = re.i() % (2*K);
            int y = re.i() % (2*K);
            int c;
            if(re.s() == "B"){
                c = 1;
            }
            else{
                c = -1;
            }
            if(x >= K){
                c *= -1;
                x -= K;
            }
            if(y >= K){
                c *= -1;
                y -= K;
            }
            if(c == -1){
                count++;
            }
            Plus(0,x,0,y,c);
            if(x != K-1){
                Plus(x+1,K-1,K,K+y,c);
            }
            if(y != K-1){
                Plus(0,x,y+K+1,2*K-1,c);
            }
            if(x != K-1 && y != K-1){
                Plus(x+1,K-1,y+1,K-1,c);
            }
        }
        for(int i=0;i<K;i++){
            for(int j=0;j<K*2;j++){
                if(i != 0){
                    Map[i,j] += Map[i-1,j];
                }
                if(j != 0){
                    Map[i,j] += Map[i,j-1];
                }
                if(i != 0 && j != 0){
                    Map[i,j] -= Map[i-1,j-1];
                }
            }
        }
        int max = -N;
        for(int i=0;i<K;i++){
            for(int j=0;j<K*2;j++){
                max = Math.Max(max,Map[i,j]);
            }
        }
        count += max;
        sb.Append(count+"\n");
    }
    void Plus(int x0,int x1,int y0,int y1,int c){
        Map[x0,y0] += c;
        if(x1 != K-1){
            Map[x1+1,y0] -= c;
        }
        if(y1 != 2*K-1){
            Map[x0,y1+1] -= c;
        }
        if(x1 != K-1 && y1 != 2*K-1){
            Map[x1+1,y1+1] += c;
        }
    }
}
class ReadData{
    string[] str;
    int counter;
    public ReadData(){
        counter = 0;
    }
    public string s(){
        if(counter == 0){
            str = Console.ReadLine().Split(' ');
            counter = str.Length;
        }
        counter--;
        return str[str.Length-counter-1];
    }
    public int i(){
        return int.Parse(s());
    }
    public long l(){
        return long.Parse(s());
    }
    public double d(){
        return double.Parse(s());
    }
    public int[] ia(int N){
        int[] ans = new int[N];
        for(int j=0;j<N;j++){
            ans[j] = i();
        }
        return ans;
    }
    public int[] ia(){
        str = Console.ReadLine().Split(' ');
        counter = 0;
        int[] ans = new int[str.Length];
        for(int j=0;j<str.Length;j++){
            ans[j] = int.Parse(str[j]);
