using System;
using System.Text;
using System.Collections.Generic;
using System.Linq;
class Solve{
    public Solve(){}
    StringBuilder sb;
    ReadData re;
    public static int Main(){
        new Solve().Run();
        return 0;
    }
    void Run(){
        sb = new StringBuilder();
        re = new ReadData();
        Calc();
        Console.Write(sb.ToString());
    }
    void Calc(){
        int A = re.i();
        int B = re.i();
        int N = 202;
        int M = 101*101+200;
        int[,] Cost = new int[101,101];
        int[,] D = new int[A,B];
        for(int i=0;i<A;i++){
            for(int j=0;j<B;j++){
                D[i,j] = re.i();
            }
        }
        for(int a=1;a<=A;a++){
            for(int b=1;b<=B;b++){
                for(int i=0;i<=100;i++){
                    for(int j=0;j<=100;j++){
                        Cost[i,j] = Math.Max(D[a-1,b-1]-a*i-b*j,Cost[i,j]);
                    }
                }
            }
        }
        for(int a=1;a<=A;a++){
            for(int b=1;b<=B;b++){
                bool OK = false;
                for(int i=0;i<=100;i++){
                    for(int j=0;j<=100;j++){
                        OK |= D[a-1,b-1] == Cost[i,j] + a*i + b*j;
                    }
                }
                if(!OK){
                    sb.Append("Impossible\n");
                    return;
                }
            }
        }
        sb.Append("Possible\n");
        sb.Append(N+" "+M+"\n");
        for(int i=1;i<=100;i++){
            sb.Append(i+" "+(i+1)+" X\n");
        }
        for(int i=102;i<=201;i++){
            sb.Append(i+" "+(i+1)+" Y\n");
        }
        for(int i=0;i<=100;i++){
            for(int j=0;j<=100;j++){
                sb.Append(i+1);
                sb.Append(" ");
                sb.Append(202-j);
                sb.Append(" ");
                sb.Append(Cost[i,j]+"\n");
            }
        }
        sb.Append(1+" "+202+"\n");
    }
}
class ReadData{
    string[] str;
    int counter;
    public ReadData(){
        counter = 0;
    }
    public string s(){
        if(counter == 0){
            str = Console.ReadLine().Split(' ');
            counter = str.Length;
        }
        counter--;
        return str[str.Length-counter-1];
    }
    public int i(){
        return int.Parse(s());
    }
    public long l(){
        return long.Parse(s());
    }
    public double d(){
        return double.Parse(s());
    }
    public int[] ia(int N){
