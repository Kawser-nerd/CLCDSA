from bisect import bisect_left
N = int(input())
c = [int(input()) for i in range(N)]
dp = [c[0]]  # dp[i]:i??????????????????????????????
for i in range(1, N):
    for j in reversed(range(len(dp))):
        if dp[j] < c[i]:
            if j + 1 == len(dp):
                dp.append(c[i])
            else:
                dp[j + 1] = c[i]
            break
    dp[0] = min(dp[0], c[i])
print(N-len(dp))