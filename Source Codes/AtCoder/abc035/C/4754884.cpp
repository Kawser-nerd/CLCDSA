int main() {
  int N, Q;
  vector<int> diff;
  vector<int> states;

  cin >> N >> Q;
  states.resize(N);
  diff.resize(N);
  for (int i=0; i<N; i++) {
    diff[i] = 0; // ????????????0??????????????0????
  }

  for (int j=0; j<Q; j++) {
    int L, R;
    cin >> L >> R;
    --L; --R; // ????1???????1-origin????????1?????
    diff[L] ^= 1;
    if (R+1 < N) {    // R+1???N??????????????????
      diff[R+1] ^= 1; 
    }
  }

  states[0] = diff[0]; // ???????????????????
  for (int i=1; i<N; i++) {
    // diff[i] = states[i-1] ^ states[i]???????????
    // ???????states[i]???????????
    states[i] = states[i-1] ^ diff[i]; 
  }

  for (int i=0; i<N; i++) {
    cout << states[i];
  }
  cout << endl;
}