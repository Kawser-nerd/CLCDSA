class Solve{
    public Solve(){}
    StringBuilder sb;
    ReadData re;
    public static int Main(){
        new Solve().Run();
        return 0;
    }
    void Run(){
        sb = new StringBuilder();
        re = new ReadData();
        Calc();
        Console.Write(sb.ToString());
    }
    void Calc(){
        int N = re.i();
        bool[] X;
        {
            string S = re.s();
            X = new bool[S.Length];
            for(int i=0;i<S.Length;i++){
                X[i] = S[S.Length-1-i] == '1';
            }
        }
        bool[] B = new bool[0];
        for(int i=0;i<N;i++){
            string S = re.s();
            bool[] C = new bool[S.Length];
            for(int j=0;j<S.Length;j++){
                C[j] = S[S.Length-1-j] == '1';
            }
            if(i == 0){
                B = C;
            }
            else{
                B = gcd(B,C);
            }
        }
        long count = 0;
        if(X.Length < B.Length){
            count = 1;
        }
        else{
            for(int i=0;i<X.Length-B.Length+1;i++){
                count *= 2;
                if(X[X.Length-1-i]){
                    count++;
                }
                count %= Define.mod;
            }
            count++;
            count %= Define.mod;
            bool[] XC = new bool[X.Length];
            for(int i=X.Length-1;i>=B.Length-1;i--){
                if(XC[i] != X[i]){
                    for(int j=i;j>=i-B.Length+1;j--){
                        XC[j] ^= B[j-i+B.Length-1];
                    }
                }
            }
            for(int i=X.Length-1;i>=0;i--){
                if(X[i] && !XC[i]){
                    break;
                }
                if(!X[i] && XC[i]){
                    count = (count + Define.mod - 1) % Define.mod;
                    break;
                }
            }
        }
        sb.Append(count+"\n");
    }
    bool[] gcd(bool[] X,bool[] Y){
        if(X.Length < Y.Length){
            bool[] temp = X;
            X = Y;
            Y = temp;
        }
        for(int i=X.Length-1;i>=Y.Length-1;i--){
            if(X[i]){
                for(int j=i;j>=i-Y.Length+1;j--){
                    X[j] = X[j] ^ Y[Y.Length-1-i+j];
                }
            }
        }
        int p = Y.Length-2;
        while(p >= 0 && !X[p]){
            p--;
        }
        if(p == -1){
            return Y;
        }
        else{
            bool[] ans = new bool[p+1];
            for(int i=0;i<=p;i++){
                ans[i] = X[i];
            }
            return gcd(ans,Y);
        }
    }
}
