import java.util.*;

public class Main {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		// N,A,B????????
		int N = sc.nextInt();
		int[] a = new int[N];
		// Alice?Bob?????????????
		int result = 0;
		// ??a?N????????
		for (int i = 0; i < N; i++) {
			a[i] = sc.nextInt();
		}
		// ?????????????????????????Integer????????????????????
		Arrays.sort(a);
		for (int i = N - 1; i >= 0; i = i - 2) {
			if (i >= 1) {
				result += a[i] - a[i - 1];
			} else {
				result = result + a[i];
			}
		}

		System.out.println(result);
		sc.close();
	}
}