void solve(int n, vector<int> &a){
    vector<int> ans(n);
    int m = n / 2;
    if(n % 2 == 0){
        for (int i=0; i<m; ++i){
            ans[i] = a[(n - 1) - 2 * i];
        }
        for (int i=0; i<m; ++i){
            ans[(n - 1) - i] = a[(n - 2) - 2 * i];
        }
    }
    else {
        for (int i=0; i<m+1; ++i){
            ans[m - i] = a[2 * i];
        }
        for (int i=0; i<m; ++i){
            ans[m + 1 + i] = a[2 * i + 1];
        }
    }
    bool isFirst = true;
    for (int i=0; i<n; ++i){
        if(isFirst){
            isFirst = false;
            printf("%d", ans[i]);
        }
        else {
            printf(" %d", ans[i]);
        }
    }
    printf("\n");
    return;
}

