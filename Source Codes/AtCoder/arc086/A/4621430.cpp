int solve(int n, int k, vector<int> &a){
    int ret = 0;
    vector<int> count(n, 0);
    for (int i=0; i<n; ++i){
        count[a[i] - 1]++;
    }
    sort(ALL(count));
    for (int i=0; i<n-k; ++i){
        ret += count[i];
    }
    return ret;
}

