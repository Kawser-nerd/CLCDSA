def b_amoeba(N, M, Board):
    ans_board = [[0] * M for _ in [None] * N]

    # ???????????????????????????
    for row in range(1, N - 1):
        for col in range(1, M - 1):
            # ??????????????????????????????
            # (????????????????????)
            v = min(Board[row - 1][col], Board[row][col + 1],
                    Board[row + 1][col], Board[row][col - 1])
            ans_board[row][col] = v

            # ans_board[row][col] ??????????Board???????
            # ?????????
            Board[row - 1][col] -= v
            Board[row][col + 1] -= v
            Board[row + 1][col] -= v
            Board[row][col - 1] -= v
    ans = '\n'.join(''.join(map(str, row)) for row in ans_board)
    return ans

N, M = [int(i) for i in input().split()]
Board = [[int(i) for i in input()] for j in range(N)]
print(b_amoeba(N, M, Board))