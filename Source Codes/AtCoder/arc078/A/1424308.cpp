using namespace std;

#define ll long long
#define ld long double
#define null NULL

const ll INF = 1e18 + 228;


ll a[228228];


signed main() {
	int n;
	scanf("%d", &n);
	ll sum = 0LL;
	for (int i = 1; i <= n; ++i) {
		scanf("%lld", &a[i]);
		sum += a[i];
	}
	ll now = 0LL;
	ll minim = INF;
	for (int i = 1; i < n; ++i) {
		now += a[i];
		ll sec = sum - now;
		if (abs(now - sec) < minim) {
			minim = abs(now - sec);
		}
	}
	cout << minim << endl;
    return 0;
}