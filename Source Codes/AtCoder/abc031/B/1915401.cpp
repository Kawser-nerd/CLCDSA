using std::min;
using std::max;
using std::sort;
using std::abs;

typedef long long int ll;
const int MOD = 1e9 + 7;

int main() {
    int l, h;
    cin >> l >> h;

    int n;
    cin >> n;

    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    for (int i = 0; i < n; i++) {
        if (a[i] > h) {
            cout << -1 << endl;
        } else {
            cout << max(l - a[i], 0) << endl;
        }
    }

    return 0;
}