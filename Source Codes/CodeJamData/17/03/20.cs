using System;
using System.Text;
using System.Collections.Generic;
using System.Linq;
class Solve{
    public Solve(){}
    StringBuilder sb;
    public static int Main(){
        new Solve().Run();
        return 0;
    }
    void Run(){
        sb = new StringBuilder();
        Calc();
        Console.Write(sb.ToString());
    }
    void Calc(){
        int Q = int.Parse(Console.ReadLine());
        for(int q=0;q<Q;q++){
            string[] str = Console.ReadLine().Split(' ');
            long N = long.Parse(str[0]);
            long K = long.Parse(str[1]);
            while(K != 1){
                if(K % 2 == 1){
                    N = (N-1)/2;
                    K = (K-1)/2;
                }
                else{
                    N = N/2;
                    K = K/2;
                }
            }
            sb.Append("Case #"+(q+1)+": "+N/2+" "+(N-1)/2+"\n");
        }
    }
}
