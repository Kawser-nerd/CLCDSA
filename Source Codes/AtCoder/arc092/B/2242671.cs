class Solve{
    public Solve(){}
    StringBuilder sb;
    ReadData re;
    public static int Main(){
        new Solve().Run();
        return 0;
    }
    void Run(){
        sb = new StringBuilder();
        re = new ReadData();
        Calc();
        Console.Write(sb.ToString());
    }
    void Calc(){
        int N = re.i();
        int[] A = re.ia();
        int[] B = re.ia();
        Array.Sort(A);
        Array.Sort(B);
        int count = 0;
        for(int i=28;i>=0;i--){
            count *= 2;
            int bordera;
            int borderb;
            int bf = 0;
            int bl = N;
            while(bf != bl){
                int bc = (bf+bl)/2;
                if(bc == N || A[bc] >= (1 << i)){
                    bl = bc;
                }
                else{
                    bf = bc+1;
                }
            }
            bordera = bf;
            if(bordera != N){
                for(int j=bordera;j<N;j++){
                    A[j] -= (1 << i);
                }
                int[] newA = new int[N];
                int p = 0;
                int q = bordera;
                int c = 0;
                while(c < N){
                    if(p < bordera && (q == N || A[p] < A[q])){
                        newA[c] = A[p];
                        p++;
                    }
                    else{
                        newA[c] = A[q];
                        q++;
                    }
                    c++;
                }
                A = newA;
            }
            bf = 0;
            bl = N;
            while(bf != bl){
                int bc = (bf+bl)/2;
                if(bc == N || B[bc] >= (1 << i)){
                    bl = bc;
                }
                else{
                    bf = bc+1;
                }
            }
            borderb = bf;
            if(borderb != N){
                for(int j=borderb;j<N;j++){
                    B[j] -= (1 << i);
                }
                int[] newB = new int[N];
                int p = 0;
                int q = borderb;
                int c = 0;
                while(c < N){
                    if(p < borderb && (q == N || B[p] < B[q])){
                        newB[c] = B[p];
                        p++;
                    }
                    else{
                        newB[c] = B[q];
                        q++;
                    }
                    c++;
                }
                B = newB;
            }
            long co = (long)(bordera)*(N-borderb) + (long)(borderb)*(N-bordera);
            {
                int q = N-1;
                for(int j=0;j<N;j++){
                    while(q >= 0 && A[j] + B[q] >= (1 << i)){
                        q--;
                    }
                    co += N - 1 - q;
                }
            }
            if(co % 2 == 1){
                count++;
            }
        }
        sb.Append(count+"\n");
    }
}
class ReadData{
    string[] str;
    int counter;
    public ReadData(){
        counter = 0;
    }
    public string s(){
        if(counter == 0){
            str = Console.ReadLine().Split(' ');
            counter = str.Length;
        }
        counter--;
        return str[str.Length-counter-1];
    }
    public int i(){
        return int.Parse(s());
    }
    public long l(){
        return long.Parse(s());
    }
    public double d(){
        return double.Parse(s());
    }
    public int[] ia(int N){
        int[] ans = new int[N];
        for(int j=0;j<N;j++){
            ans[j] = i();
        }
        return ans;
    }
    public int[] ia(){
        str = Console.ReadLine().Split(' ');
        counter = 0;
        int[] ans = new int[str.Length];
        for(int j=0;j<str.Length;j++){
            ans[j] = int.Parse(str[j]);
        }
        return ans;
    }
    public long[] la(int N){
