from collections import deque

K = int(input())
adjL = [[] for i in range(K)]
for i in range(K):
    adjL[i].append(((i + 1) % K, 1))  # i?(i+1) ????1?????
    adjL[i].append(((i * 10) % K, 0)) # i?(i*10)????0?????

# ?01BFS?
cost = [K] * K
cost[1] = 0
Q = deque([(1, 0)]) # 1??????
while Q:
    vNow, cNow = Q.popleft()

    if vNow == 0: break # 0????????
    if cNow > cost[vNow]: continue

    for v2, wt in adjL[vNow]:
        c2 = cNow + wt
        if c2 < cost[v2]:
            cost[v2] = c2
            if wt == 1:
                # ??1?????????????????
                Q.append((v2, c2))
            else:
                # ??0?????????????????
                Q.appendleft((v2, c2))

# ?1?0????????+1??????
print(cost[0] + 1)