import java.util.*;
import java.awt.*;
import java.awt.geom.Point2D;
import static java.lang.System.*;
import static java.lang.Math.*;
public class Main {
    public static void main(String[] $) {
        Scanner sc = new Scanner(in);
        int n=sc.nextInt();
        int[] c=new int[n+1];
        String s=sc.next();
        String r="";
        String l="";
        int neg=101;
        for (int i = 0; i < n; i++) {
            char a=s.charAt(i);
            if(a=='(')c[i+1]=c[i]+1;
            else c[i+1]=c[i]-1;
            neg=min(c[i+1],neg);
        }
        for (int i = 0; i < -neg; i++) {
            l+="(";
        }
        s=l+s;
        int temp=0;
        for (int i = 0; i < s.length(); i++) {
            if(s.charAt(i)=='(')temp++;
            else temp--;
        }
        for (int i = 0; i < temp; i++) {
            r+=")";
        }
        out.println(s+r);
    }
}