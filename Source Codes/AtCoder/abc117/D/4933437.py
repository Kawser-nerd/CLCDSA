N , K = map(int, input().split())
an = list(map(int,input().split()))
lenbitK = len(bin(K)) - 2
lenmax = max(len(bin(max(an))) - 2,lenbitK)
usebitK = [0] * lenmax
anbit = [0] * lenmax
for a in an:
    for i in range(len(bin(a))-2):
        if a>>i & 1 :
            anbit[i] += 1
tempK = 0
for i in reversed(range(lenbitK)):
    if (anbit[i] < ((N>>1)+(N&1)) ) and (tempK + (1<<i)  <= K) :
        usebitK[i] = 1
        tempK += (1<<i)
ans = 0
#print("tempK",tempK)
#print("usebitK",usebitK)
for j in range(lenmax):
    if usebitK[j] == 0:
        ans += anbit[j] * (1<<j)
    else:
        ans += (N - anbit[j]) * (1<<j)
