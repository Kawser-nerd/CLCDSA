def b_days(N, Observations):
    """
    N????????????????????????
    ????????????????????????????????
    ????????????????????????
    """
    from collections import OrderedDict
    # ????????????????????Python3.6??????
    # OrderedDict???
    weather_forecast = OrderedDict()
    weather_forecast['mousho'] = 0
    weather_forecast['manatsu'] = 0
    weather_forecast['natsu'] = 0
    weather_forecast['nettaiya'] = 0
    weather_forecast['fuyu'] = 0
    weather_forecast['mafuyu'] = 0

    for highest_temp, lowest_temp in Observations:
        # ??????????1???????????????
        # ?????????????????????????????
        # ???3??????????
        if '.' in highest_temp:
            highest_temp = int(highest_temp.replace('.', ''))
        else:
            highest_temp = int(highest_temp) * 10
        if '.' in lowest_temp:
            lowest_temp = int(lowest_temp.replace('.', ''))
        else:
            lowest_temp = int(lowest_temp) * 10

        if highest_temp >= 350:
            weather_forecast['mousho'] += 1
        elif 300 <= highest_temp < 350:
            weather_forecast['manatsu'] += 1
        elif 250 <= highest_temp < 300:
            weather_forecast['natsu'] += 1

        if lowest_temp >= 250:
            weather_forecast['nettaiya'] += 1

        if highest_temp >= 0 and lowest_temp < 0:
            weather_forecast['fuyu'] += 1

        if highest_temp < 0:
            weather_forecast['mafuyu'] += 1

    ans = ' '.join(map(str, weather_forecast.values()))
    return ans

N = int(input())
Observations = [input().split() for j in range(N)]
print(b_days(N, Observations))