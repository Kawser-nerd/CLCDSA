using System.Linq;
class Solve{
    List<int>[] G;
    int[] state;
    int r;
    int w;
    public Solve(){}
    StringBuilder sb;
    ReadData re;
    public static int Main(){
        new Solve().Run();
        return 0;
    }
    void Run(){
        sb = new StringBuilder();
        re = new ReadData();
        Calc();
        Console.Write(sb.ToString());
    }
    void Calc(){
        int N = re.i();
        int M = re.i();
        bool[,] Map = new bool[N,N];
        for(int i=0;i<M;i++){
            int f = re.i()-1;
            int t = re.i()-1;
            Map[f,t] = true;
            Map[t,f] = true;
        }
        G = new List<int>[N];
        for(int i=0;i<N;i++){
            G[i] = new List<int>();
            for(int j=0;j<N;j++){
                if(!Map[i,j] && i != j){
                    G[i].Add(j);
                }
            }
        }
        state = new int[N];
        List<int> Red = new List<int>();
        List<int> White = new List<int>();
        r = 0;
        w = 0;
        for(int i=0;i<N;i++){
            if(state[i] == 0){
                if(!dfs(i,1)){
                    sb.Append("-1\n");
                    return;
                }
                Red.Add(r);
                White.Add(w);
                r = 0;
                w = 0;
            }
        }
        bool[,] Possible = new bool[Red.Count+1,N+1];
        Possible[0,0] = true;
        for(int i=0;i<Red.Count;i++){
            for(int j=0;j<=N;j++){
                if(Possible[i,j]){
                    if(j+Red[i] <= N){
                        Possible[i+1,j+Red[i]] = true;
                    }
                    if(j+White[i] <= N){
                        Possible[i+1,j+White[i]] = true;
                    }
                }
            }
        }
        long count = N*N;
        for(int i=0;i<=N;i++){
            if(Possible[Red.Count,i]){
                count = Math.Min(count,i*(i-1)/2 + (N-i)*(N-i-1)/2);
            }
        }
        sb.Append(count+"\n");
    }
    bool dfs(int v,int s){
        state[v] = s;
        if(s == 1){
            r++;
        }
        else{
            w++;
        }
        bool ans = true;
        for(int i=0;i<G[v].Count;i++){
            int t = G[v][i];
            if(state[t] == s){
                return false;
            }
            else if(state[t] == 0){
                ans &= dfs(t,3-s);
            }
        }
        return ans;
    }
}
class ReadData{
    string[] str;
    int counter;
    public ReadData(){
        counter = 0;
    }
    public string s(){
        if(counter == 0){
            str = Console.ReadLine().Split(' ');
            counter = str.Length;
        }
        counter--;
        return str[str.Length-counter-1];
    }
    public int i(){
        return int.Parse(s());
    }
    public long l(){
        return long.Parse(s());
    }
    public double d(){
        return double.Parse(s());
    }
