int main() {
	int n;
	cin >> n;

	vector<PII> vp;
	int tmp;
	for (int i = 0; i < n; i++) {
		cin >> tmp;
		vp.PB(MP(tmp,i));
	}

	SORT(vp);

	for (auto&& var : vp) {
		dump(var.first);
		dump(var.second);
	}

	int max = 1, row = 1;
	int prev = vp[0].second;
	for (auto&& var : vp) {
		if (prev < var.second) {
			row++;
		}
		else {
			if (max < row) {
				max = row;
			}
			row = 1;
		}
		prev = var.second;
	}
	if (max < row) {
		max = row;
	}


	cout << vp.size() - max;
	return 0;
}