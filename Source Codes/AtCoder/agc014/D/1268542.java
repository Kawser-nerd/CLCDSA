	int dfs(int v, int p) {
		int cnt = 0;
		for (int t : g[v]) if (t != p) {
			int m = dfs(t, v);
			if (m == 2) return 2;
			cnt += m;
		}
		if (cnt > 1) return 2;
		if (p == -1 && cnt == 0) return 2;
		return 1 - cnt;
	}
	
