    int N;
    cin >> N;
    if (N < 10) {
        cout << N << endl;
        return 0;
    }

    int num = 0;
    int dnum = 0;
    int last = 0;
    while (N > 0) {
        num += N % 10;
        last = N % 10;
        N /= 10;
        dnum++;
    }
    cout << max(num, 9 * (dnum - 1) + last - 1) << endl;

    return 0;
