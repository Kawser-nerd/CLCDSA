#include <iostream>
#include <vector>
#include <algorithm>
#define int long long
using namespace std;

int n, a, b;

signed main() {
	int i, j;
	cin >> n >> a >> b;
	if (n > a * b || a + b > n + 1) { cout << -1 << endl; return 0; }
	
	vector<int> cnts;
	for (i = 0; i < b; i++) cnts.push_back(a);
	int cnt = a * b;
	
	for (i = b - 1; i > 0; i--) {
		if (cnt > n) {
			int eraseNum = min(cnt - n ,cnts[i] - 1);
			cnts[i] -= eraseNum;
			cnt -= eraseNum;
		}
	}
	
	static vector<vector<int>> v;
	v.resize(b);
	for (i = 0; i < b; i++) {
		for (j = 0; j < cnts[i]; j++) {
			v[i].push_back((b - 1 - i) * a + j);
		}
	}
	
	static vector<int> orderToVal;	//orderToVal[i] = i(>=0)??????????
	for (i = 0; i < b; i++) for (j = 0; j < v[i].size(); j++) orderToVal.push_back(v[i][j]);
	sort(orderToVal.begin(), orderToVal.end());
	orderToVal.erase(unique(orderToVal.begin(), orderToVal.end()), orderToVal.end());
	
	for (i = 0; i < b; i++) {
		for (j = 0; j < v[i].size(); j++) {
			int p = lower_bound(orderToVal.begin(), orderToVal.end(), v[i][j]) - orderToVal.begin();
			cout << p + 1;
			if (i + 1 == b && j + 1 == a) cout << endl;
			else cout << " ";
		}
	}
	return 0;
}

