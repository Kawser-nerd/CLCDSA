n = int(input())
a = list(map(int, input().split()))

a.sort()
acum = [0] * (n+1)

# ????0????0-based????????(1-indexed)
for i in range(n):
    acum[i+1] += acum[i] + a[i]

ans = 1  # i = n-1 ???????????????????

for i in reversed(range(n-1)):
    if a[i+1] <= 2 * acum[i+1]:
        ans += 1
    else:
        break

print(ans)