#include <iostream>
#include <vector>
#include <algorithm>
using namespace std;
typedef pair<int, int> P;

int mod = 998244353;
int n;
int x[200], y[200];
int power2[201];

P getPair(int pa, int pb) {
	int i;
	int tx = x[pb] - x[pa];
	int ty = y[pb] - y[pa];
	int minRet = n;
	int maxRet = -1;
	
	for (i = 0; i < n; i++) {
		int dx = x[i] - x[pa];
		int dy = y[i] - y[pa];
		if (tx * dy - ty * dx != 0) continue;
		maxRet = max(maxRet, i);
		minRet = min(minRet, i);
	}
	return P(minRet, maxRet);
}

int getCount(int pa, int pb) {
	int i;
	int tx = x[pb] - x[pa];
	int ty = y[pb] - y[pa];
	int ret = 0;
	
	for (i = 0; i < n; i++) {
		int dx = x[i] - x[pa];
		int dy = y[i] - y[pa];
		if (tx * dy - ty * dx != 0) continue;
		ret++;
	}
	return ret;
}

int count_line() {
	int i, j;
	vector<P> lines;
	
	for (i = 0; i < n; i++) {
		for (j = i + 1; j < n; j++) {
			lines.push_back(getPair(i, j));
		}
	}
	sort(lines.begin(), lines.end());
	lines.erase(unique(lines.begin(), lines.end()), lines.end());
	
	int ret = 0;
	for (i = 0; i < lines.size(); i++) {
		int k = getCount(lines[i].first, lines[i].second);
		ret += power2[k] - k - 1;
		ret %= mod;
	}
	ret += n + 1;
	return ret;
}

int main() {
	int i;
	
	cin >> n;
	for (i = 0; i < n; i++) cin >> x[i] >> y[i];
	
	power2[0] = 1;
	for (i = 1; i <= n; i++) power2[i] = (power2[i - 1] * 2) % mod;
	int k = count_line();
	int ans = (power2[n] - k + mod) % mod;
	cout << ans << endl;
	return 0;
}

